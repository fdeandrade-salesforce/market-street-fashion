// Helper to get order summaries
export function getOrderSummaries(orders: Order[]): OrderSummary[] {
  return orders.map(order => ({
    orderNumber: order.orderNumber,
    status: order.status,
    method: order.method,
    amount: order.amount,
    orderDate: order.orderDate,
    itemCount: order.items.reduce((sum, item) => sum + item.quantity, 0),
  }))
}
